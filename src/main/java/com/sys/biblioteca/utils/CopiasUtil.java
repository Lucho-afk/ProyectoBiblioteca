package com.sys.biblioteca.utils;

public class CopiasUtil {

	private int disponibles;
	private int prestamos;
	private int retrasados;
	private int reparacion;

	public int getDisponibles() {
		return disponibles;
	}

	public void setDisponibles(int disponibles) {
		this.disponibles = disponibles;
	}

	public int getPrestamos() {
		return prestamos;
	}

	public void setPrestamos(int prestamos) {
		this.prestamos = prestamos;
	}

	public int getRetrasados() {
		return retrasados;
	}

	public void setRetrasados(int retrasados) {
		this.retrasados = retrasados;
	}

	public int getReparacion() {
		return reparacion;
	}

	public void setReparacion(int reparacion) {
		this.reparacion = reparacion;
	}
}
